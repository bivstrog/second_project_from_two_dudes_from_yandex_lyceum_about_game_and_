def strLIST(q):
    for i in range(len(q)):
        for j in range(len(q[i])):
            if q[i][j] == (-2, 0):
                q[i][j] = 'None'
            else:
                q[i][j] = '(' + str(q[i][j][0]) + ', ' + str(q[i][j][1]) + ')'
    return q

